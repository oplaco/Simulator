    private void startRouteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startRouteButtonActionPerformed

        //Obtiene el nombre de la ruta
        String routeName = (String) routeListCombo.getSelectedItem();
        if(routeName==null)
        {
            return;
        }
        
        //Añade un numero alfinal para distinguirlo si ya existe otra ruta con el nombre
        String fpname = routeName;
        int i = 1;
        while(routeController.containsNamedPlan(fpname))
        {
            fpname=routeName+Integer.toString(i);
            i++;
        }

        try{
            cruiseAltitude = Double.parseDouble(cruiseAltitudeField.getText());
            ascentRate = Double.parseDouble(ascentRateField.getText());
            descentRate = Double.parseDouble(descentRateField.getText());

        } catch(Exception e)
        {
            return;
        }

        routeController.startNew(fpname,"src/routes/"+routeName+".txt",cruiseAltitude,ascentRate,descentRate);
    }//GEN-LAST:event_startRouteButtonActionPerformed

