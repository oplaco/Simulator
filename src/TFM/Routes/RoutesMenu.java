    public RoutesMenu(RouteController routeController) {
        initComponents();
        
        this.routeController=routeController;
        
        
        //Busca y añade todos los archivos de ruta que existen en el directorio routes
        File directoryPath = new File("src/routes/");
        //List of all files and directories
        String contents[] = directoryPath.list();
        if(contents==null)
        {
            return;
        }
        
        //Add routes available
        for(int i = 0 ; i< contents.length;i++)
        {
            String routename = contents[i].substring(0, contents[i].length()-4);
            routeListCombo.insertItemAt(routename, i);
        }
        routeListCombo.setSelectedIndex(0);
    }

